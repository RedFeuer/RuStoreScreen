package com.example.rustorescreen.domain.domainModel

import kotlinx.serialization.SerialName
import kotlinx.serialization.Serializable

@Serializable
enum class AppCategory {
    /**
     * Приложения — обычные приложения (не игры).
     */
    @SerialName("Приложения")
    APP,

    /**
     * Игры.
     */
    @SerialName("Игры")
    GAME,

    /**
     * Приложения для повышения производительности: редакторы, инструменты и т.д.
     */
    @SerialName("Производительность")
    PRODUCTIVITY,

    /**
     * Приложения для общения: мессенджеры, звонки, конференции.
     */
    @SerialName("Общение")
    SOCIAL,

    /**
     * Образовательные приложения: курсы, словари, обучающие платформы.
     */
    @SerialName("Образование")
    EDUCATION,

    /**
     * Развлекательные приложения: видео, мемы, развлечение.
     */
    @SerialName("Развлечения")
    ENTERTAINMENT,

    /**
     * Музыкальные приложения: плееры, стриминг, создание музыки.
     */
    @SerialName("Музыка")
    MUSIC,

    // @SerialName("Фото и видео")
    // VIDEO, // Оставлено закомментированным намеренно

    /**
     * Фото и видео — категория для приложений, связанных с фотографией и видео.
     */
    @SerialName("Фото и видео")
    PHOTOGRAPHY,

    /**
     * Здоровье и фитнес: трекеры, планы тренировок, диеты.
     */
    @SerialName("Здоровье и фитнес")
    HEALTH,

    /**
     * Спортивные приложения: результаты, расписания, тренировки.
     */
    @SerialName("Спорт")
    SPORTS,

    /**
     * Новости и медиа.
     */
    @SerialName("Новости")
    NEWS,

    /**
     * Книги и справочники.
     */
    @SerialName("Книги и справочники")
    BOOKS,

    /**
     * Бизнес-приложения: CRM, учёт, управление.
     */
    @SerialName("Бизнес")
    BUSINESS,

    /**
     * Финансовые приложения: банки, инвестиции, кошельки.
     */
    @SerialName("Финансы")
    FINANCE,

    /**
     * Образ жизни: хобби, личные интересы, бытовые сервисы.
     */
    @SerialName("Образ жизни")
    LIFESTYLE,

    /**
     * Путешествия: бронирование, гиды, путевые заметки.
     */
    @SerialName("Путешествия")
    TRAVEL,

    /**
     * Навигация и карты.
     */
    @SerialName("Навигация")
    MAPS,

    /**
     * Еда и напитки: доставка, рецепты, рестораны.
     */
    @SerialName("Еда и напитки")
    FOOD,

    /**
     * Шопинг: магазины, каталоги, скидки.
     */
    @SerialName("Шопинг")
    SHOPPING,

    /**
     * Утилиты и системные инструменты.
     */
    @SerialName("Утилиты")
    UTILITIES,

    /**
     * Приложения для прогноза погоды.
     */
    @SerialName("Погода")
    WEATHER,
}